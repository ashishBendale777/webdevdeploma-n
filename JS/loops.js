// loops in JS
// for . wile, do.....while

// every loop have initial start
// and exit condition

console.log("Hello")

//for loop is entry level loop
//i -  0  1 2 3----8 9 10
for (let i = 0; i < 10; i++) {
    //loop body
    console.log("Hellp", i)
}

///do....while is exit level loop
let j = 0
do {
    console.log("Hi", j)
    j++
} while (j < 10)

///while
let k = 0
while (k < 10) {
    console.log("Hi", k)
    k++
}


